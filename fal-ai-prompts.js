// =============================================================================
// Helper: resolve configurator template with color data
// =============================================================================
window.FAL_PROMPTS.resolveConfigurator = function(colorName, colorHex, colorTone) {
  const templates = window.FAL_PROMPTS.configurator;
  const resolved = {};
  for (const [key, template] of Object.entries(templates)) {
    resolved[key] = {
      positive: template.positive
        .replace(/\{colorName\}/g, colorName)
        .replace(/\{colorHex\}/g, colorHex)
        .replace(/\{colorTone\}/g, colorTone),
      negative: template.negative
        .replace(/\{colorName\}/g, colorName)
    };
  }
  return resolved;
};

// =============================================================================
// Helper: resolve testimonial template
// =============================================================================
window.FAL_PROMPTS.resolveTestimonial = function(gender, ageRange, carModel) {
  const t = window.FAL_PROMPTS.testimonials.template;
  return {
    positive: t.positive
      .replace(/\{gender\}/g, gender)
      .replace(/\{ageRange\}/g, ageRange)
      .replace(/\{carModel\}/g, carModel),
    negative: t.negative
  };
};

